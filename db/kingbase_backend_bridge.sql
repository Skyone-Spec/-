LEFT JOIN campus.sys_user advisor
    ON advisor.full_name = COALESCE((ext.ext_json::json) ->> 'advisor', (ext.ext_json::json) ->> 'advisorName')
   AND advisor.is_deleted = 0
WHERE COALESCE((ext.ext_json::json) ->> 'advisor', (ext.ext_json::json) ->> 'advisorName') IS NOT NULL
  AND NOT EXISTS (
      SELECT 1
      FROM campus.advisor_scope_binding existing
      WHERE existing.student_id = stu.id
        AND existing.advisor_username = advisor.student_no
        AND COALESCE(existing.class_name, '') = COALESCE(LEFT(ext.class_name, 32), '')
  );

