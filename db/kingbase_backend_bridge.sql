-- Backend bridge for the latest CAMPUS schema.
--
-- Purpose:
-- 1. Keep the current Spring Boot/JPA code working against the latest `campus.*` database.
-- 2. Materialize the legacy backend tables expected by the current application.
-- 3. Seed the legacy tables from the new normalized schema where a direct mapping exists.
--
-- Notes:
-- - This bridge is intentionally pragmatic: it favors running the backend now over a full Java-side rewrite.
-- - The sync is one-way bootstrap/import. New writes made by the current backend stay in the legacy bridge tables.
-- - Execute this after `db/kingbase_schema.sql`.

