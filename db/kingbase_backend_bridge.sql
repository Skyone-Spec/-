INSERT INTO campus.certificate_request (
    id,
    created_at,
    updated_at,
    student_id,
    certificate_type,
    status,
    reason,
    generated_pdf_path,
    current_approver_role,
    approval_level,
    withdrawal_deadline
)
SELECT
    ca.id,
    COALESCE(ca.created_at::timestamp, ar.created_at::timestamp, CURRENT_TIMESTAMP),
    COALESCE(ca.updated_at::timestamp, ar.updated_at::timestamp, CURRENT_TIMESTAMP),
    ar.requester_user_id,
    LEFT(COALESCE(ct.template_name, ar.title, ar.request_type), 100),
    CASE ar.status
        WHEN 'approved' THEN 'APPROVED'
        WHEN 'rejected' THEN 'REJECTED'
        WHEN 'canceled' THEN 'WITHDRAWN'
        WHEN 'closed' THEN 'APPROVED'
        WHEN 'in_review' THEN 'COUNSELOR_APPROVED'
        ELSE 'PENDING'
    END,
    LEFT(ar.purpose, 500),
    gf.storage_path,
    CASE ar.status
        WHEN 'submitted' THEN 'COUNSELOR'
        WHEN 'in_review' THEN 'COLLEGE_ADMIN'
        ELSE 'ARCHIVED'
    END,
    CASE ar.status
        WHEN 'in_review' THEN 2
        WHEN 'approved' THEN 3
        WHEN 'rejected' THEN 3
        WHEN 'canceled' THEN 3
        WHEN 'closed' THEN 3
        ELSE 1
    END,
    CASE
        WHEN ar.status IN ('submitted', 'in_review') THEN COALESCE(ar.submitted_at::timestamp, ar.created_at::timestamp) + INTERVAL '2 day'
        ELSE NULL
    END
FROM campus.cert_application ca
JOIN campus.affair_request ar ON ar.id = ca.request_id
LEFT JOIN campus.cert_template ct ON ct.id = ca.template_id
LEFT JOIN campus.file_object gf ON gf.id = ca.generated_pdf_file_id
WHERE NOT EXISTS (
    SELECT 1 FROM campus.certificate_request existing WHERE existing.id = ca.id
);

