INSERT INTO campus.academic_warning_record (
    id,
    created_at,
    updated_at,
    student_id,
    module_name,
    required_credits,
    earned_credits,
    recommended_courses
)
SELECT
    missing.id,
    COALESCE(report.created_at::timestamp, CURRENT_TIMESTAMP),
    COALESCE(report.updated_at::timestamp, CURRENT_TIMESTAMP),
    report.student_user_id,
    LEFT(module.module_name, 100),
    COALESCE(module.required_credits, 0)::integer,
    GREATEST(COALESCE(module.required_credits, 0) - COALESCE(missing.missing_credits, 0), 0)::integer,
    LEFT((
        SELECT string_agg(tc.course_name, '、' ORDER BY tc.course_name)
        FROM campus.aca_course_recommendation rec
        JOIN campus.aca_term_course tc ON tc.id = rec.course_id
        WHERE rec.report_id = report.id
          AND rec.module_id = module.id
    ), 500)
FROM campus.aca_audit_missing missing
JOIN campus.aca_audit_report report ON report.id = missing.report_id
JOIN campus.aca_program_module module ON module.id = missing.module_id
WHERE NOT EXISTS (
    SELECT 1 FROM campus.academic_warning_record existing WHERE existing.id = missing.id
);

INSERT INTO campus.data_import_task (
    id,
    created_at,
    updated_at,
    task_type,
    file_name,
    status,
    total_rows,
    success_rows,
    failed_rows,
    owner_id,
    owner_name,
    error_summary
)
SELECT
    job.id,
    COALESCE(job.created_at::timestamp, job.started_at::timestamp, CURRENT_TIMESTAMP),
    COALESCE(job.updated_at::timestamp, job.finished_at::timestamp, CURRENT_TIMESTAMP),
    job.job_type,
    LEFT(file.original_name, 255),
    CASE job.status
        WHEN 'running' THEN 'RUNNING'
        WHEN 'done' THEN CASE WHEN COALESCE(job.failed_rows, 0) > 0 THEN 'PARTIAL_SUCCESS' ELSE 'SUCCESS' END
        WHEN 'failed' THEN 'FAILED'
        ELSE 'CREATED'
    END,
    COALESCE(job.total_rows, 0)::integer,
    COALESCE(job.success_rows, 0)::integer,
    COALESCE(job.failed_rows, 0)::integer,
    COALESCE(starter.id, 0),
    COALESCE(starter.full_name, starter.student_no, 'system'),
    LEFT(job.error_message, 1000)
FROM campus.audit_import_job job
LEFT JOIN campus.file_object file ON file.id = job.source_file_id
LEFT JOIN campus.sys_user starter ON starter.id = job.started_by
WHERE NOT EXISTS (
    SELECT 1 FROM campus.data_import_task existing WHERE existing.id = job.id
);

